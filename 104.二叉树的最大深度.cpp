#define NULL 0

// Definition for a binary tree node.
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};


// @lc code=start
class Solution {
public:
    int max(int &&x, int &&y){
        return x>y?x:y;
    }

    int maxDepth(TreeNode* root) {
        if(root)
            return max(maxDepth(root->left)+1,maxDepth(root->right)+1);
        return 0;
    }
};
// @lc code=end

